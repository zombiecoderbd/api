async def generate_segment(name, text):
    """Generate a single audio segment"""
    output_path = os.path.join(OUTPUT_DIR, f"audio-{name}.mp3")
    tts = edge_tts.Communicate(text.strip(), VOICE, rate="-5%", pitch="+0Hz")
    await tts.save(output_path)
    size = os.path.getsize(output_path) / 1024
    print(f"  ✅ {name} — {size:.1f} KB")
    return output_path

async def main():
    print("=" * 60)
    print("Mission Barisal — Microsoft TTS Bengali Audio Report")
    print(f"Voice: {VOICE}")
    print(f"Output: {OUTPUT_DIR}")
    print("=" * 60)
    
    start_time = time.time()
    generated_files = []
    
    for name, text in SEGMENTS:
        filepath = await generate_segment(name, text)
        generated_files.append(filepath)
    
    elapsed = time.time() - start_time
    
    print("\n" + "=" * 60)
    print(f"✅ সব অডিও তৈরি হয়েছে!")
    print(f"মোট ফাইল: {len(generated_files)}")
    print(f"সময় লাগেছে: {elapsed:.1f} সেকেন্ড")
    
    # Calculate total size
    total_size = sum(os.path.getsize(f) for f in generated_files)
    print(f"মোট সাইজ: {total_size / 1024:.1f} KB ({total_size / (1024*1024):.2f} MB)")
    print("=" * 60)
    
    # Now merge all segments into one file using ffmpeg
    print("\n🔊 একত্রিত অডিও তৈরি হচ্ছে...")
    list_file = os.path.join(OUTPUT_DIR, "audio-list.txt")
    with open(list_file, "w") as f:
        for fp in generated_files:
            f.write(f"file '{fp}'\n")
    
    merged_path = os.path.join(OUTPUT_DIR, "analysis-report-full-audio.mp3")
    os.system(f"ffmpeg -y -f concat -safe 0 -i '{list_file}' -c copy '{merged_path}' 2>/dev/null")
    
    if os.path.exists(merged_path):
        merged_size = os.path.getsize(merged_path) / (1024 * 1024)
        print(f"✅ একত্রিত অডিও: {merged_path}")
        print(f"সাইজ: {merged_size:.2f} MB")
    else:
        print("⚠️ ffmpeg দিয়ে merge করা যায়নি। আলাদা ফাইলগুলো আছে।")
    
    # Clean up list file
    os.remove(list_file)

if __name__ == "__main__":
    asyncio.run(main())
